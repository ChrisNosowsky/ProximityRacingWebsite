          <br>
          <p>
            While the harshness of a cold winter looms, putting an end to real-life motorsport across America for the year, virtual action ramped up as the iSCAR season commenced this weekend. Calamity ensued as all four iSCAR series visited the virtual high-banks of Daytona International Speedway to open up the season for each respective series. Proximity Racing came into the iSCAR Speedweeks optimistic to have a successful weekend, but the chaos of Daytona ultimately claimed the team. The team netted one top five, five top tens in the four points events, with a high number of DNF results.
          </p>

          <br>
          <img class="center" src="img/blog4pic1.jpg">
          <br>
          <figcaption class="figcap text-center">Double wide racing at Daytona with Dylan Ault up front</figcaption>
          <br>

          <p>
            The weekend opened with the iSCAR Bronze Series Friday night for 40 laps at Daytona. The race saw four cautions, nine leaders, and a whopping 15 lead changes as iRacing Peak Antifreeze Series driver Logan Clampitt dominated to a victory over Cody Webster. Proximity Racing posted three of the the team’s five top tens in the event. Tre Shuttlesworth snagged fifth placed in a damaged car as several incidents occurred on the white flag, leading two laps on the night. Chase Bonser would finish seventh, leading four laps, while Chris Nosowsky placed ninth. eNASCAR Ignite Series driver Hunter O Johnson salvaged a 16th place finish. DNF’s for the team include Michael Bianchini in 23rd,and eNASCAR Ignite Series drivers Aaron Mulrooney Jr. in 32nd and Dylan Ault in 33rd.
          </p>

          <br>
          <img class="center" src="img/blog4pic3.jpg">
          <br>
          <figcaption class="figcap text-center">Restart in the iSCAR Bronze Series</figcaption>
          <br>

          <p>
            The iSCAR CCCS Designs Truck Series raced Friday night for 50 laps at Daytona. The race saw 11 different leaders and 16 lead changes, with five cautions during the race. Ryan Kendrick won in his series debut narrowly fellow rookie Jonathon Hernandez. Despite strong showings, the race ended in true disappoint as the team’s seven drivers all failed to score a top ten. Late race incidents took many of the team’s drivers out of contention. Hunter Johnson lead the way, finishing one lap down in 16th, followed by Michael DiMaggio one lap down in 17th. Dylan Ault DNF’ed two laps down in 18th, while Connor Brannon 19th. Chris Nosowsky DNF’ed in 22nd, leading the team’s only lap. Chase Bonser and Tre Shuttlesworth DNF’ed, finishing 31st and 34th respectively.
          </p>
          <br>
          <img class="center" src="img/blog4pic4.jpg">
          <br>
          <figcaption class="figcap text-center">Connor Brannon (#92) goes for a wild ride after being involved in the big one</figcaption>
          <br>
          <p>
            The weekend’s marquee event, the iSCAR TSP Cup Series Daytona 250 occurred Saturday night. The event was the first points-paying race in series history. The Thursday duels set the lineups while sending nearly a dozen drivers home without a spot in the qualifying field. The 100 lap race saw 13 leaders and a staggering 49 lead changes, with only five cautions over the course of the race. The race went deep into overtime with 108 laps completed. Logan Clampitt lead late in the event, but was turned coming to the strip as Charlie Baier picked up the win in a 3-wide race to the finish line by a narrow margin. Hunter Johnson and Michael DiMaggio each picked up top tens in the 250 with finishes of 8th and 9th respectively. Aaron Mulrooney Jr. lead two laps en route to a lead lap finish of 16th. Dylan Ault picked up 22nd from the pole position. He also lead two laps in the event. Michael Bianchini and Dylan Ault both wrecked out of the race early, finishing 37th and 38th. Brannon would lead two laps early in the race.
          </p>

          <br>
          <img class="center" src="img/blog4pic5.jpg">
          <br>
          <figcaption class="figcap text-center">Single file in the turn at Daytona</figcaption>
          <br>

          <p>
            The weekend concluded with the Peter Kenny Designs Series for 60 laps at Daytona. The race was ultimately 70 laps in length with several green white checker restarts. There were 10 leaders and 31 lead changes with a total of six cautions in the event. Cody Webster survived late race carnage to win by nearly a second over runner up Jake Silvia. Proximity Racing started the night with five cars in the top ten, including a Connor Brannon pole. The team was strong early, but each driver succumbed to individual issues. Tre Shuttlesworth lead the way with a 16th place finish and seven laps lead in a damaged vehicle. The rest of the team finished outside the top 30 in a tough night. Hunter Johnson lead nine laps in a 32nd place finish, one lap down. He was involved in an early incident as he merged dangerously on track in the first pit cycle. Dylan Ault was 33rd, one lap down as well. Chase Bonser faced critical damage early and retired in 39th place, 34 laps down with three laps lead. Aaron Mulrooney Jr. was contending for the lead when he was hooked in the same incident that collected Bonser. He finished 40th, 34 laps down with five laps lead. Connor Brannon finished 42nd, 65 laps down with internet issues.
          </p>
          <p>
            Proximity Racing heads to more high speed tracks with the iSCAR Tour this weekend. Wednesday the iSCAR Bronze Series heads to Michigan International Speedway for 50 laps. The Friday night CCCS Designs Truck Series hits Pocono Raceway for a 40 lap sprint. The TSP Cup Series will face 100 laps at Michigan International Speedway on Saturday Night. The weekend concludes with PKDS action from Auto Club Speedway under the lights for 75 laps. All of the action will be the Kiboy Motorsports Network on Youtube. Follow Proximity Racing on Facebook, Instagram, and Twitter @ProximityRacing for weekend updates.
          </p>
