        <div class="col-lg-8 col-md-10 mx-auto">
          <br>
          <img class="center" src="img/richmondwin.jpg">
          <br>
          <figcaption class="figcap text-center">Parker Retzlaff celebrates in style at Richmond.</figcaption>
          <br>
          <p>
            Thursday marked the opening round to the eNASCAR Ignite Series Playoff Opener, with Parker Retzlaff commanding raw pace over the field in victory. Behind him, Proximity Racing showed strong pace with mixed results.
          </p>

          <p>
            The heat races saw Proximity cars dominate their representative races. All five drivers were able to transfer directly from their heat races, three heat victories and two 2nd place finishes for strong starts in the night’s feature.
          </p>

          <p>
            Heat one saw the mix of a pressure free drive for Parker Retzlaff and a pressure filled defense for Sean Leasure. Fresh off of his pole setting time, Retzlaff took advantage of his pole setting time to get an excellent jump off of the start and lead all 10 laps of heat one. Leasure was not able to retain his 2nd place starting position, getting passed early for 2nd. For the last five laps, Leasure utilized his defensive driving ability to ward off Andy Trupiano’s aggressive attempts to pass, finishing third.
          </p>
          <br>
          <img class="center" src="img/seanrichmond.jpg">
          <br>
          <figcaption class="figcap text-center">Sean Leasure holds off Andy Trupiano for the final transfer spot in Heat #1.</figcaption>
          <br>
          <p>
            Heat two was more eventful for the Proximity Racing duo of Dylan Ault and Aaron Mulrooney Jr, who started 2nd and 4th respectively. Ault was able to chase down Riley Thompson for an early race lead. However, a mid-heat mistake stacked the field behind Ault, resulting in contact between Thompson and Mulrooney, collecting nearly every driver except the Proximity Racing duo. From there, Ault and Mulrooney cruised to a 1-2 sweep of heat 2.
          </p>
          <p>
            Hunter O Johnson was the lone Proximity driver in the final heat race of the night. His strong jump off of the start lead to a dominating flag to flag victory, while the field behind fell to calamity.
          </p>
          <p>
            The feature race feature highs and lows for the team, including Retzlaff’s commanding victory. Following Retzlaff was Johnson with a run from the mid-pack to second. Next for the team came Mulrooney finished a solid 7th. Sean Leasure in 10th and Dylan Ault in 13th rounded out the Proximity roster.
          </p>

          <p>
            Retzlaff had a night worth remembering for sure. A pole time lead to an easy heat victory early in the night. The feature race saw pressure from Max Cookson and Hunter Johnson, but Retzlaff was able to hold the win with a 0.8 second lead off of a strong start, managing courteous lap traffic in the late stages. So far, Retzlaff looks to stand the best chance at the $10,000 racing scholarship and opportunities to test a legends car and NASCAR Pinty’s Series car.
          </p>
          <br>
          <img class="center" src="img/hunterichmond.jpg">
          <br>
          <figcaption class="figcap text-center">Hunter O Johnson moves Max Cookson out of the way for P2 on the white flag.</figcaption>
          <br>
          <p>
            Hunter Johnson had a more eventful night, but had perhaps the most impressive run, passing strong cars en route to second place finish. He started off of the grid in 5th, and quickly advanced to third. Consistent pressure on Max Cookson paid off with a last lap pass made in the final corner. “It helps knowing that I can have a poor qualifying effort and then make it up in the race, it takes the pressure off of trying to hit a pole lap.” The Connecticut native also felt he had the pace to contend for the win but “I just couldn't beat (Cookson) on the bottom when I would get the chance to.”
          </p>

          <p>
            Aaron Mulrooney Jr. also had a strong feature, leading the mid-pack early in the race with a solid 4th early. However, a bobble while defending 4th dropped Mulrooney to 7th by the time he regained momentum. “I would say tonight was a so-called “learning experience” for me in the Ignite Modifieds. I have figured out how the car is going to react and I’ll be making sure to prepare for those reactions next week at Bristol.” Mulrooney feels learning from tonight will keep him a top 5 car going forward.
          </p>

          <p>
            Sean Leasure may have struggled to make his way through the field, but he survived the mid-pack chaos to finish 10th. The mid-race saw Leasure narrowly avoid disaster including contact with Lucas Cram that sent Cram into the infield grass as he tried to clear Leasure. While 10th was off of some of his teammates, he acknowledged the pace gap and is looking to improve on it. “There is definitely more to build on as far as consistency and speed. To some that may sound like a simple task, but it will take time and effort.”
          </p>

          <p>
            Rounding out the Proximity field was Dylan Ault, who salvaged a 13th place finish one lap down. The race was promising for Ault, starting 2nd, but he pounded the wall early while side by side with Johnson. He was able to take repairs and snag 13th from other attrition. Tonight’s poor finish doesn’t alarm his final round chances. “All I need to do is secure a starting spot in every weeks main event. This week I won my heat race from doing an amazing bump and run, got in a couple incidents which caused me to finish 13th which is fine for the first week.”
          </p>

          <p>
            The eNASCAR Ignite Series hits the high banks of Bristol for 30 laps in the virtual Coliseum August 15th at 8 PM EDT. You can catch the action on YouTube on the <a href="#">iRacing eSports Network</a>, the <a href="#"> iRacing</a> Twitch page, and the <a href="#">NASCAR</a> Facebook Page.
          </p>
