          <p>
            Round 3 of the eNASCAR Ignite Series saw iRacing's best young oval racers head to the one-groove Thompson Speedway last Thursday Night, and the difficulty in passing lead to fireworks on track under the lights. At the end of the night's chaos, it would be Parker Retzlaff who kept his winning streak alive, but it required some controversial contact to earn the win.
          </p>

          <p>
            Behind Retzlaff, Proximity Racing had its fair share of quarrels on track, leading to mixed results. Aaron Mulrooney followed Retzlaff with an impressive 3rd place finish, while lap one carnage collected Dylan Ault and Hunter Johnson, resulting in 12th and 17th place finishes for the drivers respectively. Sean Leasure failed to transfer from his Last Chance Qualifier after aggressive altercations with his competitors took him out of contention.
          </p>

          <p>
            For the first time this season, a Proximity Racing driver did not claim the quickest time trial run. Hunter Johnson qualified 2nd below Lucas Cram. Behind him, Dylan Ault qualified 6th and Aaron Mulrooney Jr. qualified 7th. Parker Retzlaff would not earn his 3rd straight pole, as a poor qualifying run left him 24th. Sean Leasure put himself behind the 8 ball wall contact left him unable to register a time.
          </p>
          <p>
            Heat one was a positive one for Proximity Racing, even if one of the two drivers entered missed the feature transfer spot. Dylan Ault started 2nd on the grid, while Sean Leasure lined up 9th after his poor qualifying attempt. Ault settled into 2nd off the start, applying consistent pressure to leader Lucas Cram. The Californian couldn't find the opportunity to make a pass happen and finished in 2nd. Leasure immediately jumped two spots to 7th in the early going then picked up two more through attrition to finish 5th in the heat for a good starting spot in the LCQ.
          </p>
          <br>
          <img class="center" src="img/blog1pic3.png">
          <br>
          <figcaption class="figcap text-center">Dylan Ault in Heat #1</figcaption>
          <br>
          <p>
            Heat two was a successful, yet anticlimactic one for Proximity Racing. Hunter Johnson took the pole with Aaron Mulrooney Jr. to his outside on the start. A clean getaway let the duo cruise to an easy 1-2 finish with Johnson taking the win. Mulrooney kept pressure, but couldn't execute a pass in the heat.
          </p>
          <br>
          <img class="center" src="img/blog1pic1.png">
          <br>
          <figcaption class="figcap text-center">Hunter O Johnson and Aaron Mulrooney Jr. in Heat #2</figcaption>
          <br>
          <p>
            Heat 4 was the next one for Proximity Racing. Parker Retzlaff faced a tall task in having to climb at least two spots for a feature transfer. By the second corner, the NASCAR K&N Pro Series driver made his way to 3rd, which included a forceful pass on Patrick Moose. Chaos behind eliminate many drivers, leaving Retzlaff to pressure Colin Slife into a mistake, while successful defending Slife's crossover. He then came close to taking the win from Max Cookson, but couldn't connect a bump and run pass.
          </p>

          <p>
            Sean Leasure was next up, facing one more chance at a transfer, starting 3rd in LCQ 2. He easily made his way to 2nd by lap two, and began applying pressure to Riley Thompson. Lap 4 saw Leasure put a bump and run onto Thompson, forcing Thompson into the wall as he caught the car. As this happened,Vincent Arthur overtook Leasure into the entry of one. Leasure was running on adrenaline as drove in deep into the entry of turn one, forcing Arthur into the wall to reclaim the lead. This was short lived, as Leasure got loose off of four fading into the mid-pack before being turned by an understandably upset Vincent Arthur.
          </p>

          <p>
            Next up for Proximity Racing was the feature race from Thompson. Hunter Johnson lined up 2nd, with Dylan Ault in 6th, Aaron Mulrooney Jr. in 7th, and Parker Retzlaff in 9th. By the end of the race, Retzlaff climbed to a victory, Mulrooney would round out the podium, and Ault and Johnson succumbed to a lap one incident.
          </p>
          <br>
          <img class="center" src="img/blog1pic2.png">
          <br>
          <figcaption class="figcap text-center">A Rough Outing for Sean Leasure</figcaption>
          <br>
          <p>
            Parker Retzlaff began a busy weekend picking up a controversial win in this feature. He survived the lap one carnage, claiming 4th by the end of lap one. Lap three put him into the podium as he passed Donovan Strauss for 3rd. Then, a consistent ten lap stint from Retzlaff and 2nd place Andy Trupiano saw the two close on leader Lucas Cram. One lap 15, Retzlaff   used his bumper to make a textbook bump and run pass for 2nd place. Just a couple laps later, the K&N hopeful made his way to the inside of Lucas Cram, washing up the track, putting a damper on Cram's day as he took the lead. Retzlaff went on to win by over a half a second for his third consecutive win.
          </p>

          <p>
            Concerning his use of contact, he cited the one-land nature of Thompson Speedway.“There was just no other way to pass tonight.” However, that wasn't a just reason for the reckless contact with Cram. “I’ll try to not to make as many enemies going forward.” Retzlaff went on to finish 5th in real life in the K&N Pro East Series from World Wide Technology Raceway last Saturday. 
          </p>

          <p>
            Aaron Mulrooney Jr. quietly snuck himself onto a Thompson Podium in the midst of calamity. Like Retzlaff, he also got through the lap one wreck clean coming out 5th. Went Retzlaff made a move on Strauss for 3rd, Mulrooney followed through, from there, it was all smooth sailing. Mulrooney inherited 3rd when Cram was forced from the lead. 
          </p>

          <p>
            Mulrooney was very happy to turn a consistent effort into a strong result. “In the eNASCAR Ignite series, we have a had a lot of issues with crashing, but I was glad to make it through with a 3rd place finish for Proximity Racing.” The young driver is already motivated for glory in the Martinsville Finale. “I’m just looking forward to Martinsville…[but am] looking for another good finish at New Hampshire.”
          </p>
          <p>
            Dylan Ault had a night worth forgetting, although he was able to salvage a solid points night from Thompson. The lap one incident which started when Hunter Johnson got loose from contact collected Ault who had nowhere to go. He kept his car on track for a 12th place finish, last car on the lead lap.
          </p>
          <br>
          <img class="center" src="img/parkerpic1.png">
          <br>
          <figcaption class="figcap text-center">Early Trouble for Hunter Johnson (#3) and Dylan Ault (#17) in the Feature</figcaption>
          <br>
          <p>
            Hunter Johnson was riding into Thompson on a performance high, following two consecutive top fives. However, Thompson played a poor hand for Connecticut native. Andy Trupiano and Johnson made slight contact, enough to send Johnson into the wall, ending his day dead last in 17th for the feature.
          </p>

